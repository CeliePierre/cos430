    /**
     * Linear Enhancement makes the features within an image more visible. 
     * It does this by increasing the range of values within the image 
     * which increases the contrast.  It involves:
     * 1. Finding the largest and smallest values within the image 
     *      (gray scale values are from 0 - black to 255 - white)
     * 2. Computing a scaling factor based on the min and max 
     *      scale = (255.0 / (max-min))
     * 3. Changing each pixel value by subtracting the minimum and then 
     * multiplying by the scaling factor (guarantees some black or almost 
     * black pixels and some white or whiter pixels).
     * @param evt The mouse clicked event
     */
    private void linearButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_linearButtonMouseClicked
        
    }//GEN-LAST:event_linearButtonMouseClicked
    
    
