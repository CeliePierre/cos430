    /**
     * A histogram is just a count of values.  To count the gray values in 
     * an image you need a place to store 256 values. For each pixel in the 
     * original image, increment that location in the histogram array. Once 
     * those values are gathered you need to accumulate the sums of the 
     * histograms in this manner: 
     *    sums[0] = histograms[0] then for each element in the sums array 
     *    sums[i] = sums[i-1] + histograms[i]
     * 
     * @param evt The mouse clicked event
     */
